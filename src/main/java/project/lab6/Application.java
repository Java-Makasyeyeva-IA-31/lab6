    private void setSelectedPanel() {
        for(Component component : tabbedPanel.getComponents()) {
            AbstractBasePanel panel = (AbstractBasePanel) component;
            panel.setDraw(false);
            panel.setActive(false);
        }
        
        infoTA.setText("");
        
        selectedPanel = (AbstractBasePanel) tabbedPanel.getSelectedComponent();
        selectedPanel.setActive(true);
    }

