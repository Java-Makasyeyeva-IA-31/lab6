    private void addApplicationKeyEvent() {
        KeyboardFocusManager.getCurrentKeyboardFocusManager()
                .addKeyEventDispatcher((e) -> {
                    if (KeyEvent.KEY_PRESSED == e.getID()) {
                        selectedPanel.handleKeyEvent(e);
                    }
                    return false;
        });
    }

    private void addTabbedPanelChangeListener() {
        tabbedPanel.addChangeListener((e) -> setSelectedPanel());
    }

     private void addIntoTAToPanel(JPanel panel) {
        ((AbstractBasePanel) panel).setInfoTA(infoTA);
    }

