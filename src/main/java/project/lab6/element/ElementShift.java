package project.lab6.element;

public class ElementShift {
    
    private final int x;
    private final int y;

    public ElementShift(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
